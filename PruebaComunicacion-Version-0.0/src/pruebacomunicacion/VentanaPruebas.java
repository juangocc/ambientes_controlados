    int longiTotal, longDatos, contTrama;
    longiTotal = 2 + 1 + 1 + 1 + 1 + 2;
    //2 "PA", 1 Longitud, 1 Origen, 1 Destino 1 Estado 2 "MR". 
    contTrama = 0;
    longDatos = 0;

    jTextArea1.setText("");
    jTextArea2.setText("");
    if (jCheckC1.isSelected()) {
        longDatos += 3;
    }
    if (jCheckC2.isSelected()) {
        longDatos += 3;
    }
    if (jCheckE1.isSelected()) {
        longDatos += 3;
    }
    if (jCheckE2.isSelected()) {
        longDatos += 3;
    }
    if (jCheckAA.isSelected()) {
        longDatos += 3;
    }
    if (jCheckP1.isSelected()) {
        longDatos += 3;
    }
    if (jCheckBX.isSelected()) {
        longDatos += 3;
    }

