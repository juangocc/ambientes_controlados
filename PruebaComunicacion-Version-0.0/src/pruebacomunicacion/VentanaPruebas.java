    trama[0] = 'P';
    trama[1] = 'A';
    trama[2] = (byte) (longDatos & 0x000000FF); //Longitud
    trama[3] = (byte) ((SelOrigen.getSelectedIndex() + 100) & 0x000000FF);
    trama[4] = (byte) ((SelDestino.getSelectedIndex() + 100) & 0x000000FF);
    trama[5] = (byte) (Integer.parseInt(jTextEstado.getText()) & 0x000000FF);

    contTrama = 6;

    if (jCheckC1.isSelected()) {
        trama[contTrama] = 'C';
        trama[contTrama + 1] = '1';
        trama[contTrama + 2] = (byte) ((selC1.getSelectedIndex()) & 0x000000FF);
        contTrama += 3;
    }
    if (jCheckC2.isSelected()) {
        trama[contTrama] = 'C';
        trama[contTrama + 1] = '2';
        trama[contTrama + 2] = (byte) ((selC2.getSelectedIndex()) & 0x000000FF);
        contTrama += 3;
    }
    byte temp = 0;
    if (jCheckE1.isSelected()) {
        System.out.println("E1");
        trama[contTrama] = 'E';
        trama[contTrama + 1] = '1';

        switch (selE1.getSelectedIndex()) {
            //Apagado
            case 0:
                temp = 0;
                break;
            //bajo
            case 1:
                temp = 1;
                break;
            //Medio
            case 2:
                temp = 2;
                break;
            //Alto
            case 3:
                temp = 4;
                break;
        }
        trama[contTrama + 2] = (byte) ((temp) & 0x000000FF);
        contTrama += 3;
    }
    if (jCheckE2.isSelected()) {
        System.out.println("E2");
        trama[contTrama] = 'E';
        trama[contTrama + 1] = '2';
        switch (selE2.getSelectedIndex()) {
            //Apagado
            case 0:
                temp = 0;
                break;
            //bajo
            case 1:
                temp = 1;
                break;
            //Medio
            case 2:
                temp = 6;
                break;
            //Alto
            case 3:
                temp = 4;
                break;
        }
        trama[contTrama + 2] = (byte) ((temp) & 0x000000FF);
        //trama[contTrama + 2] = (byte)((selE2.getSelectedIndex()) & 0x000000FF);
        contTrama += 3;
    }

    if (jCheckAA.isSelected()) {
        trama[contTrama] = 'A';
        trama[contTrama + 1] = 'A';
        trama[contTrama + 2] = (byte) ((selAA.getSelectedIndex()) & 0x000000FF);
        contTrama += 3;
    }

    if (jCheckP1.isSelected()) {
        trama[contTrama] = 'P';
        trama[contTrama + 1] = '1';
        trama[contTrama + 2] = (byte) ((selP1.getSelectedIndex()) & 0x000000FF);
        contTrama += 3;
    }

    if (jCheckBX.isSelected()) {
        trama[contTrama] = 'B';
        trama[contTrama + 1] = 'X';
        trama[contTrama + 2] = (byte) (jSlider1.getValue() & 0x000000FF);
        contTrama += 3;
    }

    trama[contTrama] = 'M';
    trama[contTrama + 1] = 'R';
    try {
        puerto.EscribirPuerto(trama);
    } catch (IOException | PortInUseException ex) {
        Logger.getLogger(VentanaPruebas.class.getName()).log(Level.SEVERE, null, ex);
    }
